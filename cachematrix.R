## This pair of functions will cache the potentially processor intensive "solve" function which calculates the inverse of a matrix.

## Returns a list of functions to be used by the cacheSolve function.
## Takes a matrix data type as an input

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setsolve <- function(solve) m <<- solve
  getsolve <- function() m
  list(set = set, get = get,
       setsolve = setsolve,
       getsolve = getsolve)
}


## This function constructs the cache that will be used to calculate the inverse of the matrix .
## Requires the results of the makeCacheMatrix function as an input.

cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  m <- x$getsolve()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setsolve(m)
  m
}
